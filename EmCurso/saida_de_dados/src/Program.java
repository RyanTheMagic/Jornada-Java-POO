public class Program {

	public static void main(String[] args) {
		//Sem quebra de Linha
		System.out.print("---");
		System.out.print("Hello World");
		System.out.print("---");
		
		System.out.println();
		
		//Com quebra de linha
		System.out.println("---");
		System.out.println("Hello World");
		System.out.println("---");
		
		//Declarando variável
		int idade = 32;
		
		System.out.println("Idade: " + idade + " anos");
	}

}
